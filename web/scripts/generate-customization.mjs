import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const webDir = path.resolve(scriptDir, '..');
const configPath = path.join(webDir, 'roomie.config.json');
const localRoot = path.join(webDir, 'public', 'roomie-local');
const outputPath = path.join(webDir, 'src', 'generated', 'roomie-customization.generated.ts');

const allowedTopLevel = new Set([
  'animations',
  'assets',
  'avatars',
  'branding',
  'emotes',
  'floor',
  'furniture',
  'rooms',
  'sfx',
  'wall',
]);

const allowedBrandingPrefixes = [
  'branding/logo.svg',
  'branding/logo-wordmark.svg',
  'branding/favicon.svg',
  'branding/app-icon.png',
  'branding/cards/',
  'branding/icons/',
];

async function pathExists(target) {
  try {
    await readdir(target);
    return true;
  } catch {
    return false;
  }
}

async function readConfig() {
  try {
    return JSON.parse(await readFile(configPath, 'utf8'));
  } catch (error) {
    throw new Error(`Unable to read ${path.relative(webDir, configPath)}: ${error.message}`);
  }
}

async function walk(dir, base = dir) {
  if (!(await pathExists(dir))) return [];
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const absolute = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...await walk(absolute, base));
      continue;
    }
    if (entry.name.startsWith('.')) continue;
    files.push(path.relative(base, absolute).split(path.sep).join('/'));
  }

  return files.sort();
}

function isRecognizedAsset(relativePath) {
  const topLevel = relativePath.split('/')[0];
  if (!allowedTopLevel.has(topLevel)) return false;
  if (topLevel !== 'branding') return true;
  return allowedBrandingPrefixes.some((prefix) => relativePath === prefix || relativePath.startsWith(prefix));
}

const config = await readConfig();
const discovered = await walk(localRoot);
const warnings = [];
const localAssets = {};

for (const assetKey of discovered) {
  if (!isRecognizedAsset(assetKey)) {
    warnings.push(`Skipped unrecognized customization asset: ${assetKey}`);
    continue;
  }
  localAssets[assetKey] = `/roomie-local/${assetKey}`;
}

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(
  outputPath,
  `// Generated by web/scripts/generate-customization.mjs. Do not edit by hand.\n\nexport const roomieCustomization = ${JSON.stringify({ ...config, localAssets, warnings }, null, 2)} as const;\n\nexport type RoomieCustomization = typeof roomieCustomization;\nexport type RoomieLocalAssetKey = keyof typeof roomieCustomization.localAssets;\n`,
  'utf8'
);

for (const warning of warnings) {
  console.warn(`[roomie-customization] ${warning}`);
}

console.log(`[roomie-customization] generated ${path.relative(webDir, outputPath)} with ${Object.keys(localAssets).length} local override(s).`);
